package net.bigblue.bb.blueapp.models.objects;


public class InvoiceDetail {

    private String invprod;
    private String prodId;
    private String amount;
    private String lbs;
    private String extAmt;
    private String category;
    private String itemDesc;
    private String returnedDate;
    private String grade;
    private String grade2;
    private String ret;
    private String soldAs;

    public String getInvprod() {
        return invprod;
    }

    public void setInvprod(String invprod) {
        this.invprod = invprod;
    }

    public String getProdId() {
        return prodId;
    }

    public void setProdId(String prodId) {
        this.prodId = prodId;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getLbs() {
        return lbs;
    }

    public void setLbs(String lbs) {
        this.lbs = lbs;
    }

    public String getExtAmt() {
        return extAmt;
    }

    public void setExtAmt(String extAmt) {
        this.extAmt = extAmt;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getItemDesc() {
        return itemDesc;
    }

    public void setItemDesc(String itemDesc) {
        this.itemDesc = itemDesc;
    }

    public String getReturnedDate() {
        return returnedDate;
    }

    public void setReturnedDate(String returnedDate) {
        this.returnedDate = returnedDate;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public String getGrade2() {
        return grade2;
    }

    public void setGrade2(String grade2) {
        this.grade2 = grade2;
    }

    public String getRet() {
        return ret;
    }

    public void setRet(String ret) {
        this.ret = ret;
    }

    public String getSoldAs() {
        return soldAs;
    }

    public void setSoldAs(String soldAs) {
        this.soldAs = soldAs;
    }
}
